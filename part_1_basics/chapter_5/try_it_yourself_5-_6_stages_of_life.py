# if-elif-else stages of life
age = 25
if age < 2:
    print("You are a baby!")
elif age < 4:
    print("You are a toddler.")
elif age < 13:
    print("You are a kid.")
elif age < 20:
    print("You are a teenager.")
elif age < 65:
    print("You are an adult.")
else:
    print("You are an elder.")
    # when we have a range, such as between 13 and 20
    # we only have to write the last number and < and the range will be covered
